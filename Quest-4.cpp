#include <iostream>
using namespace std;

int main() {
    int i = 5;
    float f = 5.5f;
    double d = 5.555555;

    cout << "int + int = " << i + i << endl;
    cout << "float + float = " << f + f << endl;
    cout << "double + double = " << d + d << endl;

    cout << "int / int = " << i / 2 << endl;
    cout << "float / float = " << f / 2 << endl;
    cout << "double / double = " << d / 2 << endl;

    return 0;
}
