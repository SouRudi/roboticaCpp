#include <iostream>
using namespace std;

int main() {
    char c = 'A';
    int i = 100;
    float f = 3.14f;
    double d = 3.1415926535;
    bool l = true;

    cout << "char: " << c << endl;
    cout << "int: " << i << endl;
    cout << "float: " << f << endl;
    cout << "double: " << d << endl;
    cout << "bool: " << l << "\n";

    return 0;
}
